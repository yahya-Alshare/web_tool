                        قياس الطول الخاص بك، وتقوم حاسبة الوزن المثالي لكل طول بشكل الكتروني بعملية <a href='../body_mass/index.html'>قياس كتلة الجسم</a> الخاص بك واعطاؤك معلومات عن الوزن المثالي الذي يجب ان تسعى إلى تحقيقه وذلك عن طريق تخسيس الوزن اذا
