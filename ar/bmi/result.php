                    <h2>طريقة انقاص الوزن</h2>
                    <p>يمكنك انقاص وزنك بدون انقاص كمية الطعام التي تتناولها وذلك عن طريق تأخير الفطور صباحا لساعتين أو ثلاثة عن وقت افطارك الحالي المعتاد، بالاضافة إلى تبكير العشاء حوالي ثلاث ساعات قبل موعد نومك، ومع الوقت سوف تلاحظ انخفاض في وزنك، بالاضافة
                        إلى ذلك يمكنك ممارسة بعض التمارين الرياضية صباحاً أو مساءً حسب الرغبة وذلك سوف يساعد في خسارة أكبر في الوزن مع الوقت.</p>
                    <p>
                        من الممكن أيضاً تخفيف الوزن عن طريق رجيم صحي يعتمد على تقليل كمية الطعام التي تتناولها بالإضافة إلى تغيير نوعية الطعام الذي تتناوله عادةً، في البداية ان تقليل كمية الطعام عن المعتاد سوف يؤدي إلى خفض كمية السعرات الحرارية الداخلة إلى الجسم، وايضاً تناول
                        أطعمة ذات سعرات حرارية (طاقة) قليلة سوف يؤدي بدوره أيضاً إلى تخفيف الوزن، وتناول الأطعمة الغنية بالألياف سوف يشعرك بالشبع لفترة طويلة وهذا ايضاً سوف يؤدي إلى تقليل كمية الطعام التي تتناولها، و شرب كوبين من الماء قبل تناول وجبة
                        الطعام من الأمور التي سوف تجعلك تشعر بالشبع بعد تناول كمية قليلة من الطعام ولا تنسى أن تقوم بمضغ الطعام في فمك لفترة أطول قبل أن تقوم بادخاله إلى معدتك أي ان تأكل ببطوء وليس بسرعة، وأخيراً عليك تنظيم الوجبات اليومية بحيث تأكل ثلاث
                        وجبات اساسية فقط قليلة السعرات الحرارية كما ذكرنا مسبقاً وان تكون أوقات هذه الوجبات منتظمة بشكل يومي بحيث وقت الإفطار محدد ووقت الغذاء أيضاً والعشاء، وعليك تقديم وقت العشاء بحيث ان يكون قبل النوم بساعتين أو ثلاثة، وبذلك سوف تكون
                        قد حققت رجيم صحي بامتياز دون الشعور بالجوع الشديد أو بخمول كبير في جسدك.
                    </p>
                    <h2>زيادة الوزن</h2>
                    <p>
                        في حال كنت تعاني من نقص في وزنك فانه عليك العمل على زيادة وزنك بطريقة صحيحة وصحية، ومن أجل زيادة الوزن فانه يجب عليك العمل على اسباب زيادة الوزن فمن خلال تناول أطعمة تحتوي على سعرات حرارية ( طاقة ) أكثر من حاجة جسمك فان ذلك بدوره سوف يؤدي تغير حالة جسمك
                        وزيادة وزنك مع الوقت،وايضاً زيادة كمية الطعام في وجبة العشاء سوف تساعد أيضاً في زيادة وزنك، وكما يقول الخبراء بأن تاخير العشاء ايضاً له علاقة طردية في زيادة الوزن، فكلما أخرت عشاءك زاد وزنك والعكس صحيح، حاول تقليل المجهود الذي
                        تبذله خلال اليوم إلى ادنى قدر ممكن لتقليل نشاط عملية الأيض وحرق السعرات الحرارية التي يكتسبها جسمك، ويجب عليك في النهاية استشارة أخصائي تغذية ليساعدك في العمل على اسباب زيادة الوزن وزيادة وزنك بشكل صحيح.
                    </p>
                    <h2>جدول السعرات الحرارية</h2>
                    <p>
                        لكي يحافظ أي شخص منا على وزنه المثالي عليه معرفة ما يحتاجه جسمه من سعرات حرارية، فمن المعروف ان جسم الإنسان حسب العمر وحسب الطول وحسب النشاط اليومي يحرق سعرات حرارية بكمية معينة وما يبتقى يصبح ذهون يخزنها الجسم، فكل شخص منا يحتاج جسمه إلى كمية معينة من
                        السعرات الحرارية، فاذا أكل الانسان اطعمة تحتوي على سعرات حرارية أكثر من ما يحتاجه جسمه فانه سوف يصبح سمين مع الوقت والعكس صحيح، فالشخص الذي يريد أن يزيد وزنه عليه تناول اطعمة فيها سعرات حرارية أكثر مما يحتاجه جسمه، اما الشخص الذي
                        يريد ان ينحف فعليه تناول اطعمة تحتوي على سعرات حرارية أقل مما يحتاجه جسمه ومع الوقت سوف ينقص وزنه، لذلك يمكنكم معرفة ما تحتويه الأطعمة من سعرات حراراية من خلال الضغط على
